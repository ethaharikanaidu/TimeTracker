    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (!txtProjectName.getText().equals("") && cmbSelectProject.getSelectedItem().toString().equals("Select Project")) {
            PnlTracker.getInstance().setProjectText(txtProjectName.getText());
        } else if (txtProjectName.getText().equals("") && !cmbSelectProject.getSelectedItem().toString().equals("Select Project")) {
            Item item = (Item) cmbSelectProject.getSelectedItem();
            PnlTracker.getInstance().setProjectText(item.id + "");
        } else {
            PnlTracker.getInstance().setProjectText("New Project");
        }
        dispose();
    }//GEN-LAST:event_btnOkActionPerformed

