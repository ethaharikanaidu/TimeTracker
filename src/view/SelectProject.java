    private void setItems() {
        ArrayList<Object[]> trackers = TrackerController.getTrackers();

        Vector model = new Vector();
        model.addElement(new Item(0, "Select Project"));
        for (int i = 0; i < trackers.size(); i++) {
            model.addElement(new Item(Integer.parseInt(trackers.get(i)[0].toString()), trackers.get(i)[1].toString()));
        }

        DefaultComboBoxModel<Item> defaultComboBoxModel = new DefaultComboBoxModel<Item>(model);

        cmbSelectProject.setModel(defaultComboBoxModel);
        getContentPane().add(cmbSelectProject);

        cmbSelectProject.setSelectedIndex(0);
    }

    private class Item {

        int id;
        String name;

        public Item(int id, String name) {
            this.id = id;
            this.name = name;
        }

        @Override
        public String toString() {
            return name;
        }

    }

